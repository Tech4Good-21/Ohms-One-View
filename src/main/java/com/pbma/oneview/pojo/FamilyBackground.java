package com.pbma.oneview.pojo;

public class FamilyBackground {
	
	private String guardianType;
	private String nameOfGuardian;
	private int familyMemberCount;
	private String familyEcoStatus;
	private int familyIncome;
	private String sourceOfIncome;
	private int personalIncome;
	
	public String getGuardianType() {
		return guardianType;
	}
	public void setGuardianType(String guardianType) {
		this.guardianType = guardianType;
	}
	public String getNameOfGuardian() {
		return nameOfGuardian;
	}
	public void setNameOfGuardian(String nameOfGuardian) {
		this.nameOfGuardian = nameOfGuardian;
	}
	public int getFamilyMemberCount() {
		return familyMemberCount;
	}
	public void setFamilyMemberCount(int familyMemberCount) {
		this.familyMemberCount = familyMemberCount;
	}
	public String getFamilyEcoStatus() {
		return familyEcoStatus;
	}
	public void setFamilyEcoStatus(String familyEcoStatus) {
		this.familyEcoStatus = familyEcoStatus;
	}
	public int getFamilyIncome() {
		return familyIncome;
	}
	public void setFamilyIncome(int familyIncome) {
		this.familyIncome = familyIncome;
	}
	public String getSourceOfIncome() {
		return sourceOfIncome;
	}
	public void setSourceOfIncome(String sourceOfIncome) {
		this.sourceOfIncome = sourceOfIncome;
	}
	public int getPersonalIncome() {
		return personalIncome;
	}
	public void setPersonalIncome(int personalIncome) {
		this.personalIncome = personalIncome;
	}

}
